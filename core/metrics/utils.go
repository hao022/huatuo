package collector

import (
	"bufio"
	"os"
)

func CountLines(path string) (int64, error) {
	f, err := os.Open(path)
	if err != nil {
		return 0, err
	}
	defer f.Close()

	var count int64
	scanner := bufio.NewScanner(f)

	// scanner.Buffer(make([]byte, 256*1024), 512*1024)

	for scanner.Scan() {
		count++
	}

	return count, scanner.Err()
}
